pasta_renomeado = "renomeado"

# Ordenar a lista de palavras em ordem decrescente de tamanho
palavras_expressoes.sort(key=lambda x: len(x), reverse=True)

def busca_em_arquivo_pdf(arquivo_pdf, palavras_expressoes):
    with open(arquivo_pdf, "rb") as pdf_file:
        pdf_reader = PyPDF2.PdfReader(pdf_file)

        pagina = pdf_reader.pages[0]
        texto_pagina = pagina.extract_text().lower()
        texto_pagina = " ".join(texto_pagina.split())  # Garante espaços únicos

        for termo in palavras_expressoes:
            if termo.lower() in texto_pagina.lower():
                return termo

    return None

if not os.path.exists(pasta_renomeado):
    os.makedirs(pasta_renomeado)

for nome_arquivo in os.listdir(pasta_pdf):
    if nome_arquivo.endswith(".pdf"):
        arquivo_pdf = os.path.join(pasta_pdf, nome_arquivo)
        palavra_encontrada = busca_em_arquivo_pdf(arquivo_pdf, palavras_expressoes)
        if palavra_encontrada:
            novo_nome_arquivo = f"{palavra_encontrada.upper().replace(' ', '_')}_{nome_arquivo}"
            novo_caminho_arquivo = os.path.join(pasta_renomeado, novo_nome_arquivo)
            shutil.move(arquivo_pdf, novo_caminho_arquivo)
            print(f"A palavra foi encontrada no arquivo: {nome_arquivo}. O arquivo foi renomeado para: {novo_nome_arquivo}")
else:
    print("As palavras ou expressões não foram encontradas em nenhum arquivo PDF na pasta.")
